# 1) Créer le répertoire tp1 dans votre répertoire d’accueil.
mkdir tp1
# 2) Créer les différentes branches de l’arbre contenant les répertoires.
cd tp1
mkdir -p users/bin/rep1 users/bin/rep2 users/web essai/sources/rep3 essai/sources/rep4 essai/data
# 3) Créer les fichiers ordinaires fic1 et fic2
touch users/bin/rep2/fic1 users/bin/rep2/fic2
# 4) Afficher le nom de votre répertoire de travail.
pwd 
# 5) Déplacez-vous dans le répertoire users que vous avez créé.
cd ~/tp1/users
# 6) Afficher le contenu de votre répertoire d’accueil sans vous déplacer et afficher le contenu de votre répertoire de travail.
ls ~
ls
# 7) Déplacez-vous dans le répertoire parent.
cd .. 
# 8) Afficher le contenu de votre répertoire d’accueil sans vous déplacer et afficher le contenu de votre répertoire de travail.
ls ~
